#!/usr/bin/env python3
"""
Module for a type-annotated function to_str that takes a float and
returns the string representation of the float.
"""


def to_str(n: float) -> str:
    """
    Returns the string representation of the float number.

    Args:
    n (float): The float number to convert to string.

    Returns:
    str: The string representation of the float number.
    """
    return str(n)
